#include "main.h"

int add(int a, int b)
{
	int add = a + b;
	return add;
}

int sub(int a, int b)
{
	int sub = a - b;
	return sub;
}

int mul(int a, int b)
{
	int mul = a * b;
	return mul;
}

int div(int a, int b)
{
	int div = a / b;
	return div;
}

int mod(int a, int b)
{
	int mod = a % b;
	return mod;
}
